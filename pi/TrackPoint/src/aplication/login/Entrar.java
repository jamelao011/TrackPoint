    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        String U, S;
        U = txtUsername.getText();
        S = txtSenha.getText();
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            java.sql.Connection conectar = DriverManager.getConnection("jdbc:mysql://localhost:3306/trackpoint", "root", "");
            PreparedStatement st = conectar.prepareStatement("SELECT * FROM usuario WHERE username=? AND senha=?");
            st.setString(1, U);
            st.setString(2, S);
            ResultSet usuario = st.executeQuery(); // executa o comando insert
            if (usuario.next()){
                String cargo = usuario.getString("Cargo");
                dispose();
                new Menu(cargo).setVisible(true);
            } else {
                 JOptionPane.showMessageDialog(null, "Usuário e/ou senha inválidos");   
            }
            txtUsername.setText("");
            txtSenha.setText("");
            txtUsername.requestFocus(); // deixa o cursor no 1 campo (usuario)
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Entre em contato com o suporte erro: " + ex.getMessage());
        }   
    }//GEN-LAST:event_btnEntrarActionPerformed

